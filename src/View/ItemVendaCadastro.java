    public boolean validarProduto() {
        if (txtCodigoProduto.getText().trim().equals("")) {
            return true;
        }

        DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            if (String.valueOf(model.getValueAt(i, 0)).trim().equals(txtCodigoProduto.getText().trim())) {
                tabelaProdutos.setRowSelectionInterval(i, i);
                return true;
            }
        }

        JOptionPane.showMessageDialog(null, "Produto não encontrado!", "Atenção", JOptionPane.WARNING_MESSAGE);
        txtCodigoProduto.grabFocus();

        if (tabelaProdutos.getRowCount() > 0) {
            tabelaProdutos.setRowSelectionInterval(0, 0);
        }

        return false;
    }

    public boolean validarObrigatorio() {
        boolean retorno = true;

        if (txtQuantidade.getText().trim().equals("0") || txtQuantidade.getText().trim().equals("")) {
            retorno = false;
            txtNome.grabFocus();
        } else if (txtCodigoProduto.getText().trim().equals("")) {
            retorno = false;
            txtCodigoProduto.grabFocus();
        }

        if (!retorno) {
            JOptionPane.showMessageDialog(null, "Campo de preenchimento obrigatório vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }

        return retorno;
    }

