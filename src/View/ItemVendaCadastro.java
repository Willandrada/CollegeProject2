    public void eventoGrade() {
        // coloca evento de mudança na tabela
        tabelaProdutos.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();
                    if (model == null || model.getRowCount() == 0 || tabelaProdutos.getSelectedRow() < 0) {
                        return;
                    }

                    txtCodigoProduto.setText(String.valueOf(model.getValueAt(tabelaProdutos.getSelectedRow(), 0)));
                    txtNome.setText(String.valueOf(model.getValueAt(tabelaProdutos.getSelectedRow(), 1)));
                    txtVlUnitario.setText(Raiz.getStringNumber(String.valueOf(model.getValueAt(tabelaProdutos.getSelectedRow(), 2))));
                    //txtQuantidade.setText(Raiz.getStringNumber(String.valueOf(model.getValueAt(tabelaProdutos.getSelectedRow(), 3))));
                }
            }
        });
    }

    public void init() {
        List<Produto> listaProdutos;
        
        listaProdutos = null;
        
        //try{
        //	if (this.chkSomenteComEstoque.isSelected()) {
        //		listaProdutos = ServicoProduto.procurarProduto("", "", 1, 0);
        //	}else{
        //		listaProdutos = ServicoProduto.procurarProduto();
        //	}
        //}catch(Exception erro){
        //}
        
        listaProdutos = ProdutoDAO.getProdutos();
        
        DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();
        model.setRowCount(0);

        //if (listaProdutos == null) {
        //	return;
        //}
        int size = listaProdutos.size();

        for (int i = 0; i < size; i++) {
            Produto produto = listaProdutos.get(i);

            if (produto != null) {
                Object[] row = new Object[4];

                row[0] = produto.getId();
                row[1] = produto.getModelo();
                row[2] = produto.getPreco();
                row[3] = produto.getQuantidade();

                model.addRow(row);
            }
        }
    }

