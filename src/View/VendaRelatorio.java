    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        String DtInicio = "", DtFim = "";
        List<Pedido> listVenda = null;

        DtInicio = txtDtVendaDe.getText();
        DtInicio = DtInicio.replace("/", "");

        DtFim = txtDtVendaAte.getText();
        DtFim = DtFim.replace("/", "");

        if (DtInicio.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Data de inicio não preenchida");
            return;
        }

        if (DtFim.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Data de fim não preenchida");
            return;
        }

        try {
            listVenda = PedidoDAO.getPedidosRel(txtDtVendaDe.getText(), txtDtVendaAte.getText());
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        DefaultTableModel model = (DefaultTableModel) tabelaResultado.getModel();
        model.setRowCount(0);

        if (listVenda == null || listVenda.size() <= 0) {
            JOptionPane.showMessageDialog(rootPane, "A pesquisa não retornou resultados ",
                    "Sem resultados", JOptionPane.ERROR_MESSAGE);

            return;
        }

        int size = listVenda.size();

        for (int i = 0; i < size; i++) {
            Pedido venda = listVenda.get(i);

            if (venda != null) {
                Object[] row = new Object[7];

                row[0] = venda.getCodigo();
                row[1] = venda.getCodigoCliente();
                row[2] = venda.getDtVenda();
                row[3] = venda.getVlTotal();
                row[4] = venda.getVlDesconto();
                row[5] = venda.getFormaPagto();
                row[6] = venda.getNotasInternas();
                model.addRow(row);
            }
        }

        // colocando colunas numéricas à direita
        DefaultTableCellRenderer alignmentDireita = new DefaultTableCellRenderer();
        alignmentDireita.setHorizontalAlignment(4); // 4 = direita

        tabelaResultado.getColumnModel().getColumn(2).setCellRenderer(alignmentDireita);
        tabelaResultado.getColumnModel().getColumn(3).setCellRenderer(alignmentDireita);

    }//GEN-LAST:event_cmdBuscarActionPerformed

