    private void txtDtVendaDeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDtVendaDeFocusLost
        if (txtDtVendaDe.getText().equals("  /  /    ")) {
            return;
        }

        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false); // Se true, reajusta a data para um valor válido. Se false, não.

        try {
            df.parse(txtDtVendaDe.getText()); // data válida
        } catch (Exception ex) { // data inválida
            txtDtVendaDe.setText("");
            txtDtVendaDeKeyReleased(null);
        }
    }//GEN-LAST:event_txtDtVendaDeFocusLost

